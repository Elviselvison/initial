# stampo tutti gli elementi di una lista 
lis = [20,30,10,15,25,]

for i in lis:
    print(i)


# sommo tutti gli elementi della lista
t = 0
 
for i in lis:
    t+=i
print('il risultat della somma  è ' +str(t))

# moltiplico  tutti gli elementi della lista
t = 1
 
for i in lis:
    t*=i
print('il risultat della moltiplicazione   è ' +str(t))

